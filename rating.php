            <h1>Как узнать рейтинг шахматиста: российский и международный</h1>
            <p>Рейтинг шахматиста определяет его уровень игры. В России существуют два официальных рейтинга:</p>
            <ul>
               <li><strong>Рейтинг ФШР</strong> – Федерация шахмат России (учет российских соревнований).</li>
               <li><strong>Рейтинг FIDE</strong> – международный рейтинг (учет мировых турниров).</li>
            </ul>

            <h2>Как узнать рейтинг шахматиста в России (ФШР)</h2>
            <ol>
               <li>Перейдите на <a href="https://ratings.ruchess.ru/" target="_blank">официальный сайт ФШР</a>.</li>
               <li>Введите <strong>ID игрока</strong> или его <strong>ФИО</strong> (можно указать год рождения и регион).</li>
               <li>Ознакомьтесь с рейтингом, статистикой и списком турниров.</li>
            </ol>

            <h2>Как узнать международный рейтинг шахматиста (FIDE)</h2>
            <ol>
               <li>Перейдите на <a href="https://ratings.fide.com/" target="_blank">официальный сайт FIDE</a>.</li>
               <li>Введите <strong>ID игрока</strong> или его <strong>ФИО</strong> с дополнительными данными.</li>
               <li>Посмотрите текущий рейтинг, динамику и список международных турниров.</li>
            </ol>

            <h2>Почему важно следить за рейтингом?</h2>
            <p>Рейтинг позволяет анализировать прогресс, выбирать подходящие турниры и оценивать уровень соперников.</p>

